package com.madebyferdi.folume.shape.svg;


final public class SvgPoint
{

	// Properties
	private double x;
	private double y;


	public SvgPoint(double x, double y)
	{
		this.x = x;
		this.y = y;
	}


	/**
	 * Getters
	 */
	public double getX()
	{
		return x;
	}

	public double getY()
	{
		return y;
	}
}
